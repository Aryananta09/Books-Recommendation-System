print(x, y)

"""Pada tahap ini, dibuat variabel x yang merepresentasikan pasangan user dan buku sebagai input fitur, serta y sebagai nilai rating yang telah dinormalisasi ke rentang 0‚Äì1. Selanjutnya, data dibagi menjadi dua bagian, yaitu 80% untuk pelatihan (train) dan 20% untuk validasi (val), yang nantinya digunakan untuk keperluan evaluasi performa model."""

class BookRecommenderNet(tf.keras.Model):

  def __init__(self, num_users, num_books, embedding_size, **kwargs):
    super(BookRecommenderNet, self).__init__(**kwargs)
    self.num_users = num_users
    self.num_books = num_books
    self.embedding_size = embedding_size

    # Embedding untuk user
    self.user_embedding = layers.Embedding(
        num_users,
        embedding_size,
        embeddings_initializer='he_normal',
        embeddings_regularizer=keras.regularizers.l2(1e-6)
    )
    self.user_bias = layers.Embedding(num_users, 1)

    # Embedding untuk buku
    self.book_embedding = layers.Embedding(
        num_books,
        embedding_size,
        embeddings_initializer='he_normal',
        embeddings_regularizer=keras.regularizers.l2(1e-6)
    )
    self.book_bias = layers.Embedding(num_books, 1)

  def call(self, inputs):
    # Ambil embedding dan bias user
    user_vector = self.user_embedding(inputs[:, 0])
    user_bias = self.user_bias(inputs[:, 0])

    # Ambil embedding dan bias buku
    book_vector = self.book_embedding(inputs[:, 1])
    book_bias = self.book_bias(inputs[:, 1])

    # Dot product
    dot_user_book = tf.tensordot(user_vector, book_vector, 2)

    # Jumlahkan semua komponen
    x = dot_user_book + user_bias + book_bias

    # Output: Skor prediksi rating (0‚Äì1 karena sigmoid)
    return tf.nn.sigmoid(x)

"""Pada tahap ini, didefinisikan sebuah class model rekomendasi berbasis neural collaborative filtering bernama BookRecommenderNet. Model ini menggunakan layer embedding untuk memetakan user dan buku ke dalam representasi vektor berdimensi tertentu, serta menambahkan bias masing-masing. Kemudian, dilakukan dot product antara embedding user dan buku, ditambah bias, dan hasil akhirnya diproses melalui fungsi aktivasi sigmoid untuk menghasilkan skor prediksi rating dalam rentang 0‚Äì1."""

model = BookRecommenderNet(num_users, num_books, embedding_size=50)
model.compile(
    loss=tf.keras.losses.BinaryCrossentropy(),
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
    metrics=[tf.keras.metrics.RootMeanSquaredError()]
)

